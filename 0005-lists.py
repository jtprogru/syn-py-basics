# Создание списка из 10 элементов с разными типами данных
my_list = ["apple", 25, True, 7.5, "banana", False, 42, "orange", 3.14, "grape"]

# Вывод списка на экран
print("Список из 10 элементов:", my_list)

# Используя срезы, вывод первых 5 элементов списка
print("Первые 5 элементов списка:", my_list[:5])

# Вывод последних 3 элементов списка
print("Последние 3 элемента списка:", my_list[-3:])

# Вывод каждого второго элемента списка
print("Каждый второй элемент списка:", my_list[::2])

# Изменение 3 элемента списка на новое значение
my_list[2] = "new value"

# Вывод измененного списка на экран
print("Измененный список:", my_list)

