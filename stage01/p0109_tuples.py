# Создание кортежа из трех целых чисел
my_tuple = (2, 4, 6)

# Вывод каждого числа из кортежа
print(f"Кортеж: {my_tuple}")
print(f"Первое число: {my_tuple[0]}")
print(f"Второе число: {my_tuple[1]}")
print(f"Третье число: {my_tuple[2]}")

# Вычисление суммы чисел в кортеже
total_sum = sum(my_tuple)
print(f"Сумма: {total_sum}")
